function detectLang() {
  var s = localStorage.getItem("foliosense_lang");
  if (s === "en" || s === "es") return s;
  return (navigator.language || "en").slice(0, 2).toLowerCase() === "es"
    ? "es"
    : "en";
}

function applyLang(lang) {
  var t = TRANSLATIONS[lang];
  if (!t) return;

  // Simple text replacements
  document.querySelectorAll("[data-i18n]").forEach(function (el) {
    var key = el.getAttribute("data-i18n");
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // Placeholder replacements
  document.querySelectorAll("[data-i18n-placeholder]").forEach(function (el) {
    var key = el.getAttribute("data-i18n-placeholder");
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // Show/hide prose blocks: <div data-lang="en"> / <div data-lang="es">
  document.querySelectorAll("[data-lang]").forEach(function (el) {
    el.style.display = el.getAttribute("data-lang") === lang ? "" : "none";
  });

  var toggle = document.getElementById("lang-toggle");
  if (toggle) toggle.textContent = t.lang_toggle;

  document.documentElement.lang = lang;
  localStorage.setItem("foliosense_lang", lang);
}

function toggleLang() {
  var c = detectLang();
  applyLang(c === "en" ? "es" : "en");
}

document.addEventListener("DOMContentLoaded", function () {
  applyLang(detectLang());
  var btn = document.getElementById("lang-toggle");
  if (btn) btn.addEventListener("click", toggleLang);
});

window.FolioSenseLang = {
  apply: applyLang,
  toggle: toggleLang,
  detect: detectLang,
};
